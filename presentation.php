      <h2><u>Le métier de développeur web</u></h2>
      <p>Le métier de développeur web consiste à créer, écrire, tester, documenter des sites Internet, des applications ou des logiciels. Le développeur travaille en équipe, le plus souvent avec des clients, des chefs de projet et d'autres développeurs.</p>
      <h2><u>Durée de la formation</u></h2>
      <p>Une nouvelle technologie ou un nouveau framework apparaît tous les 6 mois en moyenne. La Wild Code School a choisi la durée de 5 mois pour sa formation afin de se synchroniser avec ce rythme de l'évolution technologique.</p>
